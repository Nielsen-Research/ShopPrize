package com.bean;

public class Item {

	int receiptinfoId;
	int itemId;
	String itemDescription;
	String itemQuantity;
	String itemTotal;
	String rawItemDiscountDesc;
	String rawItemDiscount;
	
	
	
	public int getReceiptinfoId() {
		return receiptinfoId;
	}
	public void setReceiptinfoId(int receiptinfoId) {
		this.receiptinfoId = receiptinfoId;
	}
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public String getItemDescription() {
		return itemDescription;
	}
	public void setItemDescription(String itemDescription) {
		this.itemDescription = itemDescription;
	}
	public String getItemQuantity() {
		return itemQuantity;
	}
	public void setItemQuantity(String itemQuantity) {
		this.itemQuantity = itemQuantity;
	}
	public String getItemTotal() {
		return itemTotal;
	}
	public void setItemTotal(String itemTotal) {
		this.itemTotal = itemTotal;
	}
	public String getRawItemDiscountDesc() {
		return rawItemDiscountDesc;
	}
	public void setRawItemDiscountDesc(String rawItemDiscountDesc) {
		this.rawItemDiscountDesc = rawItemDiscountDesc;
	}
	public String getRawItemDiscount() {
		return rawItemDiscount;
	}
	public void setRawItemDiscount(String rawItemDiscount) {
		this.rawItemDiscount = rawItemDiscount;
	}
	
	
	
}
