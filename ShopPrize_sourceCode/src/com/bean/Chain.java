package com.bean;

public class Chain {

	int chainId;
	int chainName;
	
	
	public int getChainId() {
		return chainId;
	}
	public void setChainId(int chainId) {
		this.chainId = chainId;
	}
	public int getChainName() {
		return chainName;
	}
	public void setChainName(int chainName) {
		this.chainName = chainName;
	}
	
	
	
}
